  var ids = [];
  async function get_nfts() {

    var nftCount = parseInt(await nft_contract.methods.balanceOf(wallet_address).call());
    $("#nft").append(nftCount);

    var kongz = await kongz_contract.methods.balanceOf(wallet_address).call();
    $("#kongz").append(caver.utils.fromPeb(kongz));

    var promises = [];
    var data = [];
    for (var i = 0; i < nftCount; i += 1) {
      promises.push(new Promise(async (resolve) => {
        var ii = i;
        var id = parseInt(await nft_contract.methods.tokenOfOwnerByIndex(wallet_address, ii).call());
        var lastClaimBlock = parseInt(await miner_contract.methods.lastClaimBlocks(id).call());
        var claimable = await miner_contract.methods.claimable([id]).call();
        data.push({
            id,
            lastClaimBlock,
            claimable,
        });
        resolve();
      }));
    }
    await Promise.all(promises);

    data.sort((a, b) => parseFloat(caver.utils.fromPeb(b.claimable)) - parseFloat(caver.utils.fromPeb(a.claimable)));

    for (var i = 0; i < data.length; i += 1) {
        ids.push(data[i].id);
    }

    var all = await miner_contract.methods.claimable(ids).call();
    $("#all").append(caver.utils.fromPeb(all));

    for (var i = 0; i < data.length; i += 1) {
      var d = data[i];
      $("#list").append("<tr><td>" + d.id + "</td><td>" + d.lastClaimBlock + "</td><td>" + caver.utils.fromPeb(d.claimable) + " KONGZ</td><td>" +
      '<button class="btn btn-outline-success my-2 my-sm-0" onclick="claim(' + d.id + ')">KONGZ COIN 얻기</button>' + "</td>");
    }
    $("#loading").remove();
  }

